package org.itson.bdavanzadas.entidades;

/**
 * Enumeración que representa los posibles estados de una licencia.
 * Los estados pueden ser ACTIVA o INACTIVA.
 * 
 * Esta enumeración define los distintos estados que puede tener una licencia en el sistema.
 * @author José Karim Franco Valencia - 245138
 * @author Jesus Rene Gonzalez Castro - 247336
 * @author Gael Rafael Castro Molina - 247887
 */
public enum EstadoLicencia {

    /**
     * Representa una licencia activada.
     */
    ACTIVA,

    /**
     * Representa una licencia desactivada.
     */
    INACTIVA
    
}
